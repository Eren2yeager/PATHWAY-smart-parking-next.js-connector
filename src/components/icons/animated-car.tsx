import { forwardRef, useCallback, useImperativeHandle, useRef } from "react";
import { cn } from "@/lib/utils/cn";

export interface AnimatedCarIconHandle {
  startAnimation: () => void;
  stopAnimation: () => void;
}

interface AnimatedCarIconProps extends HTMLAttributes<HTMLDivElement> {
  size?: number;
}


